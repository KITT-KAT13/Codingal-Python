class vehicle:
    def __init__(self, max_speed, mileage):
        self.max_speed = max_speed
        self.mileage = mileage

Audi = vehicle(540, 100)

#accessing variables

print("Audi speed is:", Audi.max_speed)
print("Audi mileage is:", Audi.mileage)

Toyota = vehicle(500, 100)

#accessing variables

print("Toyota speed is:", Toyota.max_speed)
print("Toyota mileage is:", Toyota.mileage)







                                                                           